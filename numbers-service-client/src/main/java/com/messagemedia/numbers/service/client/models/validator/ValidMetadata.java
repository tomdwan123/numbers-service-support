package com.messagemedia.numbers.service.client.models.validator;

import javax.validation.Constraint;
import javax.validation.Payload;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import static java.lang.annotation.ElementType.*;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

@Target({FIELD, METHOD, PARAMETER, ANNOTATION_TYPE})
@Retention(RUNTIME)
@Constraint(validatedBy = MetadataValidator.class)
public @interface ValidMetadata {

    String message() default
            "Maximum of 11 entries with no keys either null, empty or longer than 100 characters, and no values longer than 256 characters.";

    Class<?>[] groups() default {};

    Class<? extends Payload>[] payload() default {};
}
